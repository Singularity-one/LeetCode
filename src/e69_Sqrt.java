/*
實作 int sqrt(int x)。
計算和回傳 x 的平方根。
x 是由正整數所組成。

Input: x = 4
Output: 2

Input: x = 8
Output: 2
Explanation: The square root of 8 is 2.82842..., 
and since the decimal part is truncated, 2 is returned.

*/

public class e69_Sqrt {
	 public int mySqrt(int x) {
	        return (int)Math.sqrt(x);
	 }
}
